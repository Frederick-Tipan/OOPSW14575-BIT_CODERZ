    private void readStaff() {
    StaffController staffReader = new StaffController();
    ArrayList<Staff> staffList = staffReader.read();

    DefaultTableModel model = (DefaultTableModel) tblReadStaff.getModel();
    model.setRowCount(0); 

    for (Staff staff : staffList) {
        Object[] rowData = {staff.getId(), staff.getType(), staff.getEmployees().getFirst().getId(), staff.getTotalStaffCost(), staff.getDaysWorked(), staff.getHoursWorked()};
        model.addRow(rowData);
    }
    }

